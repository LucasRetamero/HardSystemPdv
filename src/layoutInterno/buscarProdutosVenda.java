    private void buscarTodosProdutos(){
    try{
    pst = cone.prepareStatement("select id_produto,codbarras,reduzida,quantidade,valorlucro from produtos where  status like 'S'");
    rs = pst.executeQuery();
    tbProdutos.setModel(DbUtils.resultSetToTableModel(rs));
    }catch(SQLException error){
    JOptionPane.showMessageDialog(null,error.getMessage(),"Aviso ESSE MEMSO",JOptionPane.ERROR_MESSAGE);
    }
    }
    private void BuscarDecricaoProduto(){
     try{
    pst = cone.prepareStatement("select id_produto,codbarras,reduzida,quantidade,valorlucro  from produtos where  descricao like ? and status like 'S'");
    pst.setString(1,txtBusca.getText().toUpperCase()+"%");
    rs = pst.executeQuery();
    tbProdutos.setModel(DbUtils.resultSetToTableModel(rs));
    }catch(SQLException error){
    JOptionPane.showMessageDialog(null,error.getMessage(),"Aviso",JOptionPane.ERROR_MESSAGE);
    }
    }

