    private void tbProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProdutosMouseClicked
           if (tbProdutos.getSelectedRowCount() == 1) {
           int linha_select = tbProdutos.getSelectedRow();
            setIdProduto(tbProdutos.getValueAt(linha_select, 0).toString());
            setCodBarras(tbProdutos.getValueAt(linha_select, 1).toString());
            descricao = tbProdutos.getValueAt(linha_select, 2).toString();
            valorVenda = tbProdutos.getValueAt(linha_select, 4).toString();
            jTextField1.setText(descricao);
            saida = true;
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um produto", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tbProdutosMouseClicked

